

def binary_to_decimal(binary_number): 
    """Convert a binary to a decimal number"""
    is_one = False
    res = 0
    elements = list(binary_number)
    for element in elements: 
        if element == '1': 
            is_one = True
        elif element == '0': 
            is_one = False
        if is_one == True: 
           res += 1 
    return print(res) 

binary_to_decimal(input("Give a binary"))
